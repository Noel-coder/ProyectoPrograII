    static boolean validar(String correo) {
        String regex = ("^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$\"");
        if (!correo.contains("inlook.es") || !correo.contains(".es")) {
            return correo.matches(regex);
        } else {
            return !correo.matches(regex);
        }
    }

