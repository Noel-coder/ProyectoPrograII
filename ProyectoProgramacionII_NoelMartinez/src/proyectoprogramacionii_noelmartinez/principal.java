    static boolean validar(String correo) {
        String regex = ("^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$\"");
        if (!correo.contains("inlook.es") || !correo.contains(".es")) {
            return correo.matches(regex);
        } else {
            return !correo.matches(regex);
        }
    }

    private void bt_registrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_registrarMouseClicked
        String correo = tf_correo.getText();
        String genero = "";
        if (rb_m.isSelected()) {
            genero = "Masculino";
        } else {
            genero = "Femenina";
        }

        String usuario = tf_usuario.getText(), contraseña = jp_contraseña.getText();

        if (validar(correo)) {
            jd_registro.setModal(false);
            jd_registro.pack();
            jd_registro.setLocationRelativeTo(this);
            jd_registro.setVisible(false);
            persona p = new persona(usuario, contraseña, correo);
            //Agregar
            Dba db = new Dba("./ProyectoProgramacionII.accdb");
            db.conectar();
            if (!RevisaCorreo(correo)) {
                try {
                    int c = 0;
                    db.query.execute("INSERT INTO Registro"
                            + "(Posicion,Usuario,Genero,Correo,Contraseña)"
                            + "VALUES (' " + c + "', '" + usuario + "', '" + genero + "' , '" + correo + "' , '" + contraseña + "' )");
                    db.commit();
                    JOptionPane.showMessageDialog(this, "Te haz registrado con exito!");
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
                db.desconectar();
                tf_usuario.setText("");
                rb_m.setSelected(true);
                tf_correo.setText("");
                jp_contraseña.setText("");

            }

        } else {
            JOptionPane.showMessageDialog(this, "Correo ingresado ya existe", "Revise sus dato por favor!", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_bt_registrarMouseClicked

    public boolean RevisaCorreo(String correo) {
        Dba db = new Dba("./ProyectoProgramacionII.accdb");
        db.conectar();
        try {
            db.query.execute("SELECT Correo FROM Registro");
            ResultSet rs = db.query.getResultSet();
            while (rs.next()) {
                if (correo.equals(rs.getString(1))) {
                    return true;
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        db.desconectar();
        return false;
    }


