    public boolean RevisaCorreo(String correo) {
        Dba db = new Dba("./ProyectoProgramacionII.accdb");
        db.conectar();
        try {
            db.query.execute("SELECT Correo FROM Registro");
            ResultSet rs = db.query.getResultSet();
            while (rs.next()) {
                if (correo.equals(rs.getString(1))) {
                    return true;
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        db.desconectar();
        return false;
    }


